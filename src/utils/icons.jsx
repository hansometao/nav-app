export function Icon({ name, size = 24, className = '', color = 'currentColor' }) {
  const svg = SVG_ICONS[name];
  if (!svg) return null;

  return (
    <span
      className={`inline-icon ${className}`}
      style={{
        display: 'inline-flex',
        alignItems: 'center',
        justifyContent: 'center',
        width: size,
        height: size,
        color: color,
      }}
      dangerouslySetInnerHTML={{ __html: svg }}
    />
  );
}

export function iconToString(name, size = 24) {
  const svg = SVG_ICONS[name];
  if (!svg) return '';

  const encoded = encodeURIComponent(svg);
  return `data:image/svg+xml,${encoded}`;
}
